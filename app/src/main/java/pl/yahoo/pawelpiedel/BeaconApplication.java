package pl.yahoo.pawelpiedel;

import android.app.Application;
import android.content.Context;

import com.facebook.stetho.Stetho;
import com.singhajit.sherlock.core.Sherlock;
import com.squareup.leakcanary.LeakCanary;
import com.tspoon.traceur.Traceur;

import pl.yahoo.pawelpiedel.injection.component.AppComponent;
import pl.yahoo.pawelpiedel.injection.component.DaggerAppComponent;
import pl.yahoo.pawelpiedel.injection.module.AppModule;
import timber.log.Timber;

public class BeaconApplication extends Application {

    private AppComponent appComponent;

    public static BeaconApplication get(Context context) {
        return (BeaconApplication) context.getApplicationContext();
    }

    @Override
    public void onCreate() {
        super.onCreate();

        if (BuildConfig.DEBUG) {
            Timber.plant(new Timber.DebugTree());
            Stetho.initializeWithDefaults(this);
            LeakCanary.install(this);
            Sherlock.init(this);
            Traceur.enableLogging();
        }
    }

    public AppComponent getComponent() {
        if (appComponent == null) {
            appComponent = DaggerAppComponent.builder()
                    .appModule(new AppModule(this))
                    .build();
        }
        return appComponent;
    }

    // Needed to replace the component with a test specific one
    public void setComponent(AppComponent appComponent) {
        this.appComponent = appComponent;
    }
}
